    <p>Nafalo est une plateforme de commerce électronique spécialisée dans la vente de produits numériques (e-books, formations en ligne, licences logicielles, templates, codes d'accès, etc.).</p>
    <p>La plateforme met à disposition des marchands des outils permettant de créer une boutique en ligne, de gérer leurs produits digitaux et d'encaisser des paiements auprès de leurs clients, principalement en Afrique francophone et dans le monde entier.</p>
    <div class="highlight-box">
        <i class="fas fa-info-circle"></i> <strong>Nafalo agit en qualité d'intermédiaire technique</strong> entre les marchands et leurs clients. Nafalo n'est pas vendeur des produits proposés sur la plateforme.
