<div class="legal-section" id="art4">
    <h2><span class="section-num">4</span> Commissions et paiements</h2>
    <h3>4.1 Commission Nafalo</h3>
    <p>Nafalo prélève une commission de <strong>5% sur chaque transaction réussie</strong>. Cette commission est automatiquement déduite du montant de la vente avant reversement au marchand.</p>
    <h3>4.2 Reversements</h3>
    <p>Les sommes dues au marchand (montant de la vente moins la commission Nafalo) sont reversées selon les modalités définies dans l'espace marchand. Les délais de reversement dépendent des prestataires de paiement partenaires.</p>
    <h3>4.3 Devises</h3>
    <p>Les transactions sont effectuées en <strong>Franc CFA (FCFA)</strong> pour les marchés d'Afrique de l'Ouest. D'autres devises peuvent être disponibles selon les pays et les prestataires de paiement disponibles.</p>
    <h3>4.4 Frais de transaction</h3>
    <p>Des frais supplémentaires peuvent être appliqués par les prestataires de paiement (Mobile Money, cartes bancaires, etc.). Ces frais sont à la charge du client final sauf mention contraire dans la boutique du marchand.</p>
</div>

